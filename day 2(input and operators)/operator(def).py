#Operators in python are special symbols or keywords used to perform operations on operands(variables and values).

#operators: these are the special symbols/keywords Eg: +, -, *, /, %, **, //, ==, !=, >, <, >=, <=, and, or, not, is, in

#operand:It is the value on which the operator operates. Eg: 2, 3, 4, 5, 6, 7, 8, 9, 10

#examples: addition, subtraction, multiplication, division, modulus, exponentiation, floor division, comparison operators, logical operators, identity operators, membership operators.

# +,-,*,/,%,**,//,==,!=,>,<,>=,<=,and,or,not,is,in

#Arithmetic operators: +, -, *, /, %, **, //
#comparison(Relational) operators: ==, !=, >, <, >=, <=
#logical operators: and, or, not
#identity operators: is, is not
#membership operators: in, not in
#Assignment operators: =, +=, -=, *=, /=, %=, **=, //=
#Bitwise operators: &, |, ^, ~, <<, >>
